public class DataTypes{
    public static void main(String[] args) {
        // Primitive data types are those that cannot be broken into further smaller data types

        // below are primitive data types
        char letter = 'b';
        int rollno = 69;
        float marks = 85.67f;
        double largeDecimalValues = 8578598.58859;
        long largeIntegers = 98483893823294L;
        Boolean check = true;

        // String is not a primitive data type
        String name = "Brihad";
    }
}